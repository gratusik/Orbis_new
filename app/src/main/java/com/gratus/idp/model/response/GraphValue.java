package com.gratus.idp.model.response;

public class GraphValue  {
    private int count;
    private String key;

    public GraphValue() {
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }
}
